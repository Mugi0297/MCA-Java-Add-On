package session_01;

public class DataTypes {

	public static void main(String[] args) {
		//Integer
		byte b = 10;
		short s = 100;
		long l = 10000L;
		int i = 25;
		
		//character
		char c = 'M';
		
		//Boolean
		boolean b1 = true;
		
		//Floating point
		float f = 25.36f; //7
		double d = 78.3456985;//15
		
		//Non-primitive
		String name = "Mugilan";
		
		System.out.println(name);
		
	}
}






