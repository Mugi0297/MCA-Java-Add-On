package session_02;

public class AssignmentEx {

	public static void main(String[] args) {
		int a = 5;
		
		System.out.println(a+=5); //a = a+5 -->5+5 =10
		System.out.println(a-=5); //a = a-5 -->10-5=5
		System.out.println(a*=5); //a = a*5 -->5*5=25
		System.out.println(a/=5); //a = a/5 -->25/5=5
		System.out.println(a%=5); //a = a%5 -->5%5=0
		
		
	}
}
